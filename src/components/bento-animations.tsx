// Function to get the animation component based on the variant
export const getAnimationByVariant = (variant: AnimationVariant) => {
  switch (variant) {
    case "one":
      return <SkeletonOne />;
    case "two":
      return <SkeletonTwo />;
    case "three":
      return <SkeletonThree />;
    case "four":
      return <SkeletonFour />;
    case "five":
      return <SkeletonFive />;
    default:
      return null;
  }
};
